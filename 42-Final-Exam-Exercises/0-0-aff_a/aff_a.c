#include <unistd.h>


int main(int argc, char **argv)
{
	int ind;

	ind = 0;
	if (argc!=2)
		write(1, "a\n", 2);
	else
	{
		while (argv[1][ind] != '\0')
		{
			if(argv[1][ind] == 'a')
			{
				write(1,"a\n",2);
				return (0);
			}
			else
				ind++;
		}
		write(1, "\n", 1);
		return (0);
	}
}
