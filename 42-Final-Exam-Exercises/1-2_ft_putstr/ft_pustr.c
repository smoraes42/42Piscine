#include <unistd.h>

void	ft_putstr(char *str)
{
	while (*str)
		write(1,str++,1);
}

int main(void)
{
	char v[10] = "abcdefgh";
	ft_putstr(v);
	write(1,"\n",1);

	return (0);
}
